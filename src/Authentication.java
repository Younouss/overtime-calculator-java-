    public void Login(){
        Connect con = new Connect();
        Connection connection = con.getConnection();
        String query = "";
        query = "SELECT * FROM  user WHERE username = '"+jTextField1.getText().toString()+"' AND password = '"+jPasswordField1.getText().toString()+"' AND status = 1";
        //System.out.println(jTextField_username.getDocument().getLength());
       Statement st;
       ResultSet rs;
       try{
           st = connection.createStatement();
           rs = st.executeQuery(query);
           if(rs.next() == true)
           {
               JOptionPane.showMessageDialog(null, "Connection r√©ussie");
               dispose();
               Input input = new Input();
               input.setVisible(true);
               input.setUsername(jTextField1.getText().toString());
               
           }else{
               JOptionPane.showMessageDialog(null, "Mot de passe ou compte utilisateur incorrect");
           }
       }catch(Exception ex){
           ex.printStackTrace();
       }
    }

